package primeiroModulo;

public class Principal {
	
	public static void main(String[] args) {
		// Impressão no console
		System.out.println("Hello Codenation");
		
		// existem vários tios de tipos (java é uma linguagem fortemente tipada), antes de declarar uma variável, temos que dizer o tipo.
		String nome = "Daniele";
		System.out.println("Hello " + nome);
		
		// depois que criamos a variável texto, podemos usar o . para acessar os metodos disponíveis para esse objeto.
		String texto = "Meu texto aqui";
		// texto.
		System.out.println(texto);
		
		
	}

}
